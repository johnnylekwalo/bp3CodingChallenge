package com.bp3.backend.model;

import com.bp3.backend.Edge;

public class EdgeModel implements Edge {
    String from;
    String to;

    public EdgeModel(String from, String to) {
        this.from = from;
        this.to = to;
    }

    @Override
    public String getFrom() {
        return from;
    }

    @Override
    public void setFrom(String from) {
        this.from = from;
    }

    @Override
    public String getTo() {
        return to;
    }

    @Override
    public void setTo(String to) {
        this.to = to;
    }
}
